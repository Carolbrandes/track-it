import User from '@/models/User';
import { NextResponse } from 'next/server';
import dbConnect from '../../../lib/db';


export async function POST(req: Request) {
    await dbConnect();

    const { email, code } = await req.json();

    if (!email || !code) {
        return NextResponse.json({ success: false, message: 'Email e c√≥digo s√£o obrigat√≥rios' }, { status: 400 });
    }

    const user = await User.findOne({ email });

    if (!user || user.verificationCode !== code) {
        return NextResponse.json({ success: false, message: 'C√≥digo inv√°lido' }, { status: 400 });
    }

    // Limpar o c√≥digo de verifica√ß√£o
    user.verificationCode = undefined;
    await user.save();

    const response = NextResponse.json({ success: true });

    // üî• Definir o cookie `authToken` corretamente
    response.cookies.set('authToken', user._id.toString(), {
        httpOnly: true, // ‚ùå Impede acesso no frontend
        secure: process.env.NODE_ENV === 'production',
        sameSite: 'strict', // üîí Corrigido para min√∫sculo!
        maxAge: 60 * 60 * 24 * 7, // 1 semana
        path: '/',
    });

    return response;
}